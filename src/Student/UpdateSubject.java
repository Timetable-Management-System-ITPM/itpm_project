    public void LoadSubjectNo()
    {
        try {
            pst = con.prepareStatement("select id from subject");
            rs = pst.executeQuery();
            txtsid.removeAllItems();
            while(rs.next())
            {
                txtsid.addItem(rs.getString(1));
            }
        } catch (SQLException ex) {
            Logger.getLogger(UpdateSubject.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
