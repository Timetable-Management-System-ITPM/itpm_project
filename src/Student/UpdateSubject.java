    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        

        try {
            String name = updateSubjectName.getText();
            String code = updateSubjectCode.getText();
            String year = updateSubjectYear.getText();
            String semester = updateSubjectSemester.getText();
            String pid = txtsid.getSelectedItem().toString();
            
            pst= con.prepareStatement("update subject set name=?, code=?, year=?, semester=? where id=?");
            pst.setString(1, name);
            pst.setString(2, code);
            pst.setString(3, year);
            pst.setString(4, semester);
            pst.setString(5, pid);
            int k = pst.executeUpdate();
            
            if(k==1)
            {
      
                JOptionPane.showMessageDialog(this, "Record Updated");
                updateSubjectName.setText("");
                updateSubjectCode.setText("");
                updateSubjectYear.setText("");
                updateSubjectSemester.setText("");
                updateSubjectName.requestFocus();
                LoadSubjectNo();
                
            }else{
                JOptionPane.showMessageDialog(this, "Record Failed");
            }
            
            
            // TODO add your handling code here:
        } catch (SQLException ex) {
            Logger.getLogger(UpdateSubject.class.getName()).log(Level.SEVERE, null, ex);
        }


        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

