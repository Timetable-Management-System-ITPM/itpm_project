    private void btnupdtstugrpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdtstugrpActionPerformed
       
        DefaultTableModel Df = (DefaultTableModel)jtable_stugrp.getModel();
        int selectedIndex = jtable_stugrp.getSelectedRow();
                            
            try {
                  
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/timetablemanagement","root","");
            upd = con.prepareStatement("update student_group  set yearseme= ?,programme= ?,groupno= ?,subgrpno= ?,groupid= ?,subgrpid= ? where sg_id= ?");
            
            String yearseme = txtcmbyrseme.getSelectedItem().toString();
            String programme = txtcmbprog.getSelectedItem().toString();
            String groupno = jSpinnergno.getValue().toString();
            String subgrpno = jSpinnersubgno.getValue().toString();
            String groupid = txtgid.getText();
            String subgrpid = txtsgid.getText();
          
            
            upd.setString(1, yearseme);
            upd.setString(2, programme );
            upd.setString(3, groupno);
            upd.setString(4, subgrpno);
            upd.setString(5, yearseme+"."+programme+"."+groupno);
            upd.setString(6, yearseme+"."+programme+"."+groupno+"."+subgrpno);
            upd.setString(7,Df.getValueAt(selectedIndex, 0).toString());
            
            if(yearseme.isEmpty() || programme.isEmpty() || groupno.isEmpty() || subgrpno.isEmpty())
            {
                JOptionPane.showMessageDialog(this,"Please fill required details!");
            }
            else{
                upd.executeUpdate();
                JOptionPane.showMessageDialog(this,"Record updated");
                table_update();
           
            txtcmbyrseme.setSelectedItem("");
            txtcmbprog.setSelectedItem("");
            txtgid.setText("");
            txtsgid.setText("");
            txtgid.requestFocus();
            txtsgid.requestFocus();
            }
            
            }
            catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(ManageStuGrp.class.getName()).log(Level.SEVERE, null, ex);
            }
            
    }//GEN-LAST:event_btnupdtstugrpActionPerformed

