    public void table_view() 
    {
        int c;
    
    try {
              Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/timetablemanagement","root","");
            PreparedStatement add = con.prepareStatement("select * from subject_tags");
        ResultSet Rs = add.executeQuery();
        
        ResultSetMetaData rd = Rs.getMetaData();
        c = rd.getColumnCount();
        DefaultTableModel df = (DefaultTableModel)jtable_tag.getModel();
        df.setRowCount(0);
        while(Rs.next())
        {
         Vector v2 = new Vector();
         
         for(int i=1; i<=c; i++)
         {
             v2.add(Rs.getString("tid"));
             v2.add(Rs.getString("tagname"));
             v2.add(Rs.getString("tagcode"));
             v2.add(Rs.getString("relatedtag"));
         }
         df.addRow(v2);
        }
       
         } catch (ClassNotFoundException | SQLException ex) {
        Logger.getLogger(ManageTag.class.getName()).log(Level.SEVERE, null, ex);
    }
    
    }
    
