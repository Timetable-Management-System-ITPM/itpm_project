    private void btnupdtagActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdtagActionPerformed
        
     DefaultTableModel d1 = (DefaultTableModel)jtable_tag.getModel();
        
         int selectedIndex = jtable_tag.getSelectedRow();
        
         try{
            int id = Integer.parseInt(d1.getValueAt(selectedIndex,0).toString());
            String tagname = txttagname.getText();
            String tagcode = txttagcode.getText();
            String relatedtag = txtcmbreltag.getSelectedItem().toString(); 
              
            Class.forName("com.mysql.cj.jdbc.Driver");
         Connection con = DriverManager.getConnection("jdbc:mysql://localhost/timetablemanagement","root","");
          
         PreparedStatement add = con.prepareStatement("update subject_tags set tagname= ?,tagcode= ?, relatedtag=?  where tid = ?");
            add.setString(1,tagname);
            add.setString(2,tagcode);
            add.setString(3,relatedtag);
            add.setInt(4,id);
            
            add.executeUpdate();
            
            JOptionPane.showMessageDialog(this,"Record Updated");
            
           
                txttagname.setText("");
                txttagcode.setText("");
                txtcmbreltag.setSelectedIndex(-1);  
                table_view(); 

          } catch (ClassNotFoundException | SQLException ex) {
              Logger.getLogger(ManageTag.class.getName()).log(Level.SEVERE, null, ex);
          }
    }//GEN-LAST:event_btnupdtagActionPerformed

