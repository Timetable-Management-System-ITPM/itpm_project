    private void btndeltagActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeltagActionPerformed
      
      DefaultTableModel d1 = (DefaultTableModel)jtable_tag.getModel();
        
         int selectIndex = jtable_tag.getSelectedRow();
          
           try{

            int id = Integer.parseInt(d1.getValueAt(selectIndex,0).toString());
            
            int dialogResult = JOptionPane.showConfirmDialog (null, "Do you want to Delete","Warning",JOptionPane.YES_NO_OPTION);
            
            if(dialogResult == JOptionPane.YES_OPTION)
            {
         
              Class.forName("com.mysql.cj.jdbc.Driver");
              
              Connection con = DriverManager.getConnection("jdbc:mysql://localhost/timetablemanagement","root","");
              
              PreparedStatement add = con.prepareStatement("delete from subject_tags where tid =?");
              
              add.setInt(1, id);
              
              add.executeUpdate();
              
             
               JOptionPane.showMessageDialog(this,"Record Deleted");
            
           
                txttagname.setText("");
                txttagcode.setText("");
                txtcmbreltag.setSelectedIndex(-1);  
                table_view(); 
           
            }
   
          } catch (ClassNotFoundException | SQLException ex) {
              Logger.getLogger(ManageTag.class.getName()).log(Level.SEVERE, null, ex);
          }
            
    }//GEN-LAST:event_btndeltagActionPerformed

