    private void btnaddtagActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddtagActionPerformed
        // TODO add your handling code here: 
        String tagname = txttagname.getText();
        String tagcode = txttagcode.getText();
        String relatedtag = txtcmbreltag.getSelectedItem().toString(); 
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
             con = DriverManager.getConnection("jdbc:mysql://localhost/timetablemanagement","root","");
             add = con.prepareStatement("insert into subject_tags(tagname,tagcode,relatedtag)values(?,?,?)");
                add.setString(1,tagname);
                add.setString(2,tagcode);
                add.setString(3,relatedtag);
                add.executeUpdate();
                
                 if(tagname.isEmpty() || tagcode.isEmpty() || relatedtag.isEmpty()) 
            {
                JOptionPane.showMessageDialog(this,"Please fill required details!");
            }
          else  {
               
                JOptionPane.showMessageDialog(null,"Record Added");
                 }
                 
                txttagname.setText("");
                txttagcode.setText("");
                txtcmbreltag.setSelectedIndex(-1);  
                txttagname.requestFocus();
                txttagcode.requestFocus();
        } 
        
             catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(AddTag.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    
    }//GEN-LAST:event_btnaddtagActionPerformed

