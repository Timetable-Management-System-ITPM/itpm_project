    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        
        try {
            String lecture1 = SelectedLecturer1.getText();
            String lecture2 = SelectedLecturer2.getText();
            String subcode = SelectSubjectCode.getSelectedItem().toString();
            String subname = SelectSubjectName.getSelectedItem().toString();
            String groupid = SelectGroup.getSelectedItem().toString();
            String tagname = SelectedTag.getText();
            String no_of_student = NoOfStudent.getText();
            String duration = Duration.getText();
            
            pst= con.prepareStatement("insert into session(lecture1,lecture2,subcode,subname,groupid,tagname,no_of_student,duration)values(?,?,?,?,?,?,?,?)");
            pst.setString(1, lecture1);
            pst.setString(2, lecture2);
            pst.setString(3, subcode);
            pst.setString(4, subname);
            pst.setString(5, groupid);
            pst.setString(6, tagname);
            pst.setString(7, no_of_student);
            pst.setString(8, duration);
            int k = pst.executeUpdate();
            
            if(k==1)
            {
                JOptionPane.showMessageDialog(this, "New Record Added");
                SelectedLecturer1.setText("");
                SelectedLecturer2.setText("");
                SelectSubjectCode.setSelectedIndex(-1);
                SelectSubjectName.setSelectedIndex(-1);
                SelectGroup.setSelectedIndex(-1);
                SelectedTag.setText("");
                NoOfStudent.setText("");
                Duration.setText("");
                SelectedLecturer1.requestFocus();
                 
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Record failed");
            }
            
            // TODO add your handling code here:
        } catch (SQLException ex) {
            Logger.getLogger(select_group.class.getName()).log(Level.SEVERE, null, ex);
        }


        manage_session magse = new manage_session();
        magse.show();
        
        dispose();
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton7ActionPerformed

