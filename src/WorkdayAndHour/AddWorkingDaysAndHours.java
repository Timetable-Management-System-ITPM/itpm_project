    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        
        String days = spindays.getValue().toString();
        String hours = spinhours.getValue().toString();
        String minutes = spinminutes.getValue().toString();
        int mon;
        int tue;
        int wed;
        int thu;
        int fri;
        int sat;
        int sun;
        
        if(cbmon.getState()== true){
            mon = 1;
        }else{
            mon =0 ;
        }
         if(cbtue.getState()== true){
            tue = 1;
        }else{
            tue =0 ;
        }
         if(cbwed.getState()== true){
            wed = 1;
        }else{
            wed =0 ;
        }
          if(cbthu.getState()== true){
            thu = 1;
        }else{
            thu =0 ;
        }
       if(cbfri.getState()== true){
            fri = 1;
        }else{
            fri =0 ;
        }
        if(cbsat.getState()== true){
            sat = 1;
        }else{
            sat =0 ;
        }
         if(cbsun.getState()== true){
            sun = 1;
        }else{
            sun =0 ;
        }
         
          try{
                       
            String sql = "INSERT INTO `workinghours`(`days`, `hours`, `mins`, `mon`, `tue`, `wed`, `thu`, `fri`, `sat`,`sun`) VALUES('"+days+"','"+hours+"','"+minutes+"','"+mon+"','"+tue+"','"+wed+"','"+thu+"','"+fri+"','"+sat+"','"+sun+"')";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.execute();
            
            JOptionPane.showMessageDialog(rootPane, "Working Hours Added Successfully!");
            
            
        
        }catch(Exception e){
            
            JOptionPane.showMessageDialog(rootPane, e);
        
        }
                
    }//GEN-LAST:event_jButton3ActionPerformed

