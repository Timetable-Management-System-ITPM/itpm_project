         private void table_load(){
          
            int c;
            try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/timetablemanagement","root","");
            pst = con.prepareStatement("select * from session");
            
            ResultSet rs = pst.executeQuery();
            ResultSetMetaData Rss = rs.getMetaData();
            c = Rss.getColumnCount();
            
            DefaultTableModel Df = (DefaultTableModel)jtable_consecutive.getModel();
            DefaultTableModel Df1 = (DefaultTableModel)jtable_parallel.getModel();
            DefaultTableModel Df2 = (DefaultTableModel)jtable_nonoverlap.getModel();
            Df.setRowCount(0);
            
            while(rs.next())
            {
                Vector v2 = new Vector();
                for(int a=1; a<=c; a++)
                {
                    v2.add(rs.getString("session_id"));
                    v2.add(rs.getString("lecture1"));
                    v2.add(rs.getString("lecture2"));
                    v2.add(rs.getString("subcode"));
                    v2.add(rs.getString("subname"));
                    v2.add(rs.getString("groupid"));
                    v2.add(rs.getString("tagname"));
                    v2.add(rs.getString("no_of_student"));
                    v2.add(rs.getString("duration")); 
                }
                Df.addRow(v2);
                 Df1.addRow(v2);
                  Df2.addRow(v2);
                
            }
             
        }
            catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(AddAllocateSession.class.getName()).log(Level.SEVERE, null, ex);
        }
            
         }
         
